
public class ForLoops {

	public static void main(String[] args) {

		for (int i = 0; i < 4; i++) {
			System.out.println("Hugo is super old");
		}
		
		for (int age = 25; age < 31; age++) {
			System.out.println("Hugo was " + age + " years old");
		}
		
		int[] years = {1984, 1991, 2001};
		
		for (int i = 0; i < years.length; i++) {
			System.out.println(years[i]);
		}
	}
		
}
