
public class stringmethods {

	public static void main(String[] args) {

		String pedro = "Pedro Cabrial";
		
		System.out.println(pedro);
		System.out.println(pedro.toUpperCase());
		System.out.println(pedro.toLowerCase());
		System.out.println("First char " + pedro.charAt(0));
		System.out.println("Length " + pedro.length());
		System.out.println("Last char " + pedro.charAt(12));
		System.out.println(pedro.substring(6, 13));
	}

}
